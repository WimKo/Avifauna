require(leaflet)

######################################################################################################
# Laad data
######################################################################################################

path=paste(getwd(), "/1. Data/Raw Data/Grenzen/VOG/VOG2.kml", sep="")
VOG <- readOGR(path, "Naamloze laag")
VOG=spTransform(VOG, CRS("+proj=longlat +datum=WGS84 +no_defs +ellps=WGS84 +towgs84=0,0,0"))

######################################################################################################
# Toon waarnemingen op kaart
######################################################################################################

p=leaflet() %>% 
  addTiles(group = "OpenStreetMap") %>% 
  addPolygons(data=VOG,
              weight = 4, 
              stroke=TRUE, color="White", 
              opacity=1,
              group = "Gebieden")
if (plot_map==TRUE) {print(p)}

######################################################################################################
# Bewaar data
######################################################################################################

path=paste(getwd(), "/1. Data/Cleaned Data/VOG.RData", sep="")
save(VOG,file=path)
